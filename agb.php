قوانین زیر ضمیمه‌ای بر قوانین عمومی شرکت می‌باشند. شما باید با قوانین عمومی آشنا باشید تا بدانید که چه کارهایی مجاز بوده و انجام چه کارهایی خلاف محسوب می‌شود، مخصوصاً در مواردی که بخاطر نقض قانونی بازداشت شده باشید. 
<br /><br /><br />
همکاری و یا حمایت از سایرین برای نقض قوانین، تحریک یا تشویق بازیکن‌ها جهت نقض قوانین، مجاز نبوده و جریمه در پی خواهد داشت.همه این قوانین برای همه اکانت‌ها حتی اکانت‌های حذف شده و اکانت‌هایی که در حال حذفند نیز بدون هیچ استثنایی صادق است.
<br /><br />
§1 رمز اکانت، ثبت نام و مالکیت
<br />
هر بازیکنی تنها می‌تواند یک اکانت در هر جهان بازی (سرور) داشته باشد.
<br /><br />
§1.1 ثبت نام
<br />
ایمیلی که برای ثبت نام استفاده می‌شود باید یک ایمیل شخصی بوده و ثبت نام کننده باید دسترسی کامل و کنترل کامل این ایمیل را داشته باشد. استفاده از ایمیل مشترک، ایمیل‌هایی که تاریخ انقضا دارند مجاز نیست.
دارنده ایمیل ثبت نام بدون هیچ قید و شرطی صاحب اکانت به حساب می‌آید و هر گونه قرارداد کتبی یا شفاهی با سایرین (بازیکن‌ها، اتحاد و ...) فاقد اعتبار است.
صاحب اکانت مسئول تمامی اتفاقاتی است که با اکانت رخ می‌دهد.
<br /><br />
§1.2 رمز اکانت
<br />
صاحب اکانت نمی‌تواند رمز خود را به بازیکنی که در همان جهان بازی (سرور) اکانت دارد بدهد. بعلاوه انتخاب آگاهانه رمز یکسان با سایرین خلاف است. هر یک از این اعمال مذکور مولتی اکانت به حساب آمده و تابع جریمه مولتی اکانت که در همین قوانین آمده است، خواهد بود. 
<br />
دادن رمز اکانت به بازیکنی که در همان جهان بازی (سرور) بازی نمی‌کند و در جهان بازی دیگری مشغول بازیست و یا کسی که کلاً بازی نمی‌کند ایرادی ندارد و می‌توانید با هم بر روی یک اکانت بازی کنید. 
<br />
تراوین هیچ مسئولیتی در قبال خسارات وارده بر اکانت توسط کسی که رمز اکانت را در اختیار دارد نمی‌پذیرد و در صورت هرگونه نقض قوانین توسط فردی که رمز اکانت را در اختیار دارد مطابق قوانین برخورد خواهد شد.
<br /><br />
§1.3 تغییر ایمیل اکانت / واگذاری اکانت به غیر
<br />
برای تغییر ایمیل خود و یا برای واگذاری اکانت خود به شخص دیگری که در همان جهان بازی (سرور) بازی نمی‌کند، به قسمت "اکانت" در پروفایل خود مراجعه نمایید و اطلاعات را تغییر دهید.
<br /><br />
§1.4 تعویض اکانت با بازیکن دیگر
<br />
جهت تعویض اکانت با بازیکن دیگر در همان جهان بازی (سرور)، هر دو بازیکن باید ایمیلی به آدرس admin@travian-t4.ir با ایمیل فعلی ثبت شده برای اکانت ارسال کنند. ایمیل ارسالی باید شامل اطلاعات زیر باشد (هر کدام در سطری جداگانه):
<br />
نامه باید با متن کوتاهی مانند "درخواست تعویض اکانت با اطلاعات زیر را دارم" شروع شود.
<br />
جهان بازی (سروری) که اکانت در آن قرار دارد.
<br />
نام اکانت خودتان (نه نام دهکده)
<br />
آدرس ایمیل صاحب جدید اکانت
<br />
سپس باید هر دو بازیکن از طریق تابع "رمز خود را فراموش کرده‌ام" که با یکبار ورود رمز بطور اشتباه ظاهر می‌شود می‌توانند درخواست رمز جدید بکنند. 
<br /><br />
در صورتی که خود بازیکن‌ها شخصاً اقدام به تعویض اکانت با یکدیگر کنند و یا به جای دریافت رمز جدید از طریق تابع "رمز خود را فراموش کرده‌ام" رمز را از یکدیگر سوال کنند، تراوین هیچ مسئولیتی در قبال خسارات وارده بر اکانت منجمله از دست دادن اکانت را نخواهد پذیرفت.
<br /><br /><br />
§2 جانشینی و استفاده از یک کامپیوتر
<br /><br />
§2.1 جانشینی
<br />
هر بازیکنی حق داشتن دو جانشین برای اکانت خود را دارد که در زمان غیبتش به جای وی بازی کنند. جانشین باید کاملاً به نفع اکانت بازی کند و هر گونه سوءاستفاده از جانشینی خلاف بوده و می‌تواند جریمه برای جانشین در پی داشته باشد. 
جانشین باید از طریق جانشینی وارد اکانت کسی که جانشینش است بشود. جانشین مجاز به داشتن رمز اکانت و ورود به اکانت با رمز خود صاحب اکانت را ندارد و در صورت رعایت نکردن این امر هر دو اکانت مشمول قانون §1.2 که در همین قوانین ذکر شده است خواهند بود. 
جانشینی مداوم بر اکانتی که صاحب اکانت بیش از 2 هفته غیبت داشته باشد مجاز نبوده و حکم داشتن بیش از یک اکانت برای جانشین را خواهد داشت. در صورتی که غیبت صاحب اکانت بیش از 2 هفته بطول خواهد انجامید، باید قبل از ترک اکانت توسط صاحبش، با ارسال پیام داخل بازی به مولتی هانتر، وی را از این امر مطلع سازید. 
خسارت وارده توسط جانشین، از طرف تراوین قابل ترمیم نخواهد بود. صاحب اکانت مسئول اعمال انجام شده توسط جانشین یا جانشین‌ها خود خواهد بود. در صورتی که جانشین‌ها قوانین بازی و یا قوانین عمومی شرکت تراوین را نقض کنند، صاحب اکانت و جانشین هر دو ممکن است مشمول جریمه شوند.
<br /><br />
§2.2 استفاده از یک کامپیوتر
<br />
اگر بازیکن‌هایی که از یک کامپیوتر استفاده می‌کنند بخواهند به اکانت یکدیگر دسترسی داشته باشند، این عمل حتماً باید از طریق جانشینی باشد.
<br /><br />
§3 استفاده از برنامه های خارجی
<br />
بازی باید با مرورگری (براوسری) دستکاری نشده و معمولی انجام شود. استفاده از هرگونه اسکریپت و بات که اتوماتیک عمل می‌کنند، خلاف قوانین بازی است.
<br /><br />
§4 خطاهای برنامه 
<br />
از خطاهای برنامه (که به آنها باگ نیز گفته می‌شود) نباید جهت نفع شخصی استفاده شود. سوءاستفاده از این امر می‌تواند باعث جریمه اکانت شود.
<br /><br />
§5 تبادل پول
<br />
هر گونه خرید و فروش که در آن پول واقعی رد و بدل شود حال در مورد اکانت، نیرو، دهکده، منابع، ارائه خدمات و یا هر مورد مربوط به تراوین باشد خلاف است. همچنین فروش اکانت حتی واگذاری اکانت بدون دریافت پول و بعنوان هدیه از طریق سایت‌های حراجی غیر مجاز است.
<br /><br />
§6 رفتار و رعایت حقوق اجتماعی
<br />
تمامی افراد باید با لفظی مودبانه با همدگیر برخورد کنند. مولتی‌هانترها می‌توانند پروفایل‌ها و نام دهکده‌ها و قهرمان‌ها و ... را که مناسب نباشند بدون هشدار تغییر دهند.
<br />
اعمال زیر خلاف بوده و ممکن است جریمه در پی داشته باشد و شامل تمامی قسمت‌های بازی منجمله نام اکانت، نام اتحاد، قسمت توضیحات پروفایل، نام دهکده نام قهرمان و پیام‌ها می‌شود: 
<br />
زبان فارسی و انگلیسی تنها زبان‌های قابل قبول می‌باشند. 
<br />
شرکت در هرگونه مسائلی که توهین آمیز بوده، جنبه سوءاستفاده داشته، نژادپرستانه بوده و یا بی‌حرمتی محسوب شود و همچنین مطالبی که جنس خاصی (مونث، مذکر) را مورد توهین یا تمسخر قرار دهد. همچنین هرگونه بحثی که برای دین و مذهب، نژاد، ملیت، جنسیت، گروه سنی خاصی توهین آمیز تلقی شود. تهدید کسی در زندگی واقعی. 
