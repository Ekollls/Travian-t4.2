§7 جرائم
<br />
اگر خلافی در مورد قوانین ذکر شده در بالا صورت بگیرد، مولتی‌هانترها، در صورت نیاز ادمین‌ها اکانت یا اکانت‌ها را بازداشت خواهند کرد و در مورد میزان جریمه تصمیم گیری خواهند کرد. جریمه همواره از سود حاصل از تخلف به مراتب بیشتر خواهد بود. 
منابع، ساختمان‌ها، دهکده‌ها و نیروهای از دست رفته در دوران بازداشت جزو جریمه به حساب نمی‌آیند و تیم تراوین خسارت وارد را جبران نخواهد کرد. هیچ بازیکنی حق درخواست پلاس یا سکه‌ی طلای تراوین یا پول از دست رفته خود در زمان بازداشت یا بدلیل جریمه را ندارد. 
تیم تراوین هیچ تمایزی بین بازیکن‌هایی که از سکه‌ی طلای تراوین و پلاس استفاده می کنند و سایر بازیکن‌ها قائل نخواهد شد چه در زمان بررسی تخلف و چه به هنگام جریمه کردن. 
<br />
بازیکن‌ها می‌توانند با مولتی‌هانتری که آنها را بازداشت کرده است و یا با ادمین از طریق پیام داخل بازی و یا ایمیل تماس بگیرد. در مورد جریمه و یا حذف اکانت نباید در مکان‌های عمومی بحث شود (برای مثال در فروم، چت، پیام داخل بازی و...). نامه‌های نوشته شده برای مولتی‌هانتر باید دارای موضوع مناسب بوده و به زبان فارسی و با حروف فارسی نوشته شود، مولتی‌هانتر یا ادمین یا پشتیبانی می‌تواند از خواندن و پاسخگویی به نامه‌هایی که فاقد شرایط فوق باشند، امتنا کند. 
بعلاوه تیم تراوین به هیچ عنوان هیچ اطلاعاتی در اختیار کسی جز صاحب اکانت قرار نخواهد داد (در مورد دلیل بازداشت، میزان جریمه و ...) 
<br />
مولتی اکانت‌ها (کسانی که بیش از یک اکانت داشته باشند) چه در سرورهای معمولی و چه در سرورهای اسپید، که کمتر از 100 نفر جمعیت دارند، به محض مشاهده بدون هشدار حذف خواهند شد.
